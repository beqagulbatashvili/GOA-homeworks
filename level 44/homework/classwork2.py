# 1. Beginner - Lost Without a Map
def maps(a):
    return [x * 2 for x in a]

# 2. Opposite number
def opposite(number):
    return -number

# 3. Sum Arrays 
def sum_arrays(flower1, flower2):
    return (flower1 % 2) != (flower2 % 2)

# 4. Beginner Series
def paperwork(n, m):
    if n < 0 or m < 0:
        return 0
    return n * m

# 5. Make Upper Case
def make_upper_case(s):
    return s.upper()