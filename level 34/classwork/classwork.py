def კალკულატორი(num1, num2, ოპერატორი):
    if ოპერატორი == '+':
        print(num1, "+", num2, "=", num1 + num2)
    elif ოპერატორი == '-':
        print(num1, "-", num2, "=", num1 - num2)
    elif ოპერატორი == '*':
        print(num1, "*", num2, "=", num1 * num2)
    elif ოპერატორი == '/':
        if num2 != 0:
            print(num1, "/", num2, "=", num1 / num2)


კალკულატორი(10, 5, '+')
კალკულატორი(20, 4, '*')
კალკულატორი(15, 3, '/')
